#include <iostream>
using namespace std;
int main() {
    int a=5, b=10;
    // arithmetic operators 
    cout << "a+b=" << a+b<< endl;
    cout << "a-b=" << a-b<< endl;
    cout << "a/b=" << a/b<< endl;
    cout << "a*b=" << a*b<< endl;
    cout << "a%b=" << a%b<< endl;
    // relational operators 
    cout<< (a<b) << endl;
    cout<< (a>b) << endl;
    cout<< (a==b) << endl;
    cout<< (a<=b) << endl;
    cout<< (a<=b) << endl;
    cout<< (a!=b) << endl;
    // logical operators || && !
    cout<< (a<b || a>0) << endl;
    cout<< (a<b && a>0) << endl;
    cout<< (a<b != a>0) << endl;
    

}